lista_idades = [15, 18, 20, 22, 17, 19]

print("O numero de alunos na lista é:", len(lista_idades))

print("A maior idade é:", max(lista_idades))

print("A menor idade é:", min(lista_idades))

print("A soma de todas as idades é:", sum(lista_idades))

media = sum(lista_idades) / len(lista_idades)

print("A média das idades é:", media)
